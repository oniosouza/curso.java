package aulas;

public class arraysContendo_for {
    public static void main(String[] args) {

        String[] nomes = new String[4];
        nomes[0] = "onio";
        nomes[1] = "aline";
        nomes[2] = "lavinia";
        nomes[3] = "laura";

        for (int i = 0; i < 4; i++) {
            System.out.println(nomes[i]);
        }
    }
}
